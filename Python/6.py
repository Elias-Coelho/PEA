n = int (input("Digite um número de 0 a 5: "))
if (n == 0):
 print ("O número que você digitou foi zero")
elif (n == 1):
 print ("O número que você digitou foi um")
elif (n == 2):
    print ("O número que você digitou foi dois")
elif (n == 3):
    print ("O número que você digitou foi três")
elif (n == 4):
    print ("O número que você digitou foi quatro")
elif (n == 5):
    print ("O número que você digitou foi cinco")
else:
    print("Número inválido")                          